            A custom command-line utility to seamlessly update the movie database. <br>
            <strong>delete:movies:</strong> This command removes all the movies from our database, ensuring a fresh start before new data is fetched and stored. <br>
            <strong>fetch:movies:</strong> This is the core command that drives the entire process. It initiates the movie fetching and storage operation as outlined above.
        </p>
        <ul class="mt-4 list-disc list-outside pl-5">
            <li class="text-gray-600 text-sm leading-relaxed">Before initiating any new fetch, the command first clears the old movie records.</li>
            <li class="text-gray-600 text-sm leading-relaxed">Fetches trending movies from the TDBM API. For each movie retrieved, the command inspects and updates or inserts the data into the database. This ensures that the movie collection remains fresh and current, reflecting the latest trends.</li>
            <li class="text-gray-600 text-sm leading-relaxed">Upon successfully storing the movies, the command provides a success message, confirming the correct execution and storage.</li>
